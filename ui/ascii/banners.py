    @staticmethod
    def get_loading_animation(style: str = "dots") -> str:
        """Retorna animación de carga."""
        animations = {
            "dots": ["⠋", "⠙", "⠹", "⠸", "⠼", "⠴", "⠦", "⠧", "⠇", "⠏"],
            "arrow": ["←", "↖", "↑", "↗", "→", "↘", "↓", "↙"],
            "braille": ["⣾", "⣽", "⣻", "⢿", "⡿", "⣟", "⣯", "⣷"],
            "simple": ["-", "\\", "|", "/"],
        }
        return animations.get(style, animations["dots"])
    
    @staticmethod
    def get_separator(style: str = "line", width: int = 60) -> str:
        """Retorna separador decorativo."""
        separators = {
            "line": "─" * width,
            "double": "═" * width,
            "dotted": "·" * width,
            "dashed": "─" * width,
            "stars": "✧" * width,
            "custom": None
        }
        return separators.get(style, separators["line"])
